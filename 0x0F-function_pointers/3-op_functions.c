#include "3-calc.h"

/**
 * op_add - a function that prints the sum of two numbers.
 *
 * @a: The first number.
 * @b: The second number.
 *
 * Return: The sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function that prints the sub of two numbers.
 *
 * @a: The first number.
 * @b: The second number.
 *
 * Return: The subtraction of two numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function that prints the multy of two numbers.
 *
 * @a: The first number.
 * @b: The second number.
 *
 * Return: The multipliction of two numbers.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a function that prints the div of two numbers.
 *
 * @a: The first number
 * @b: The second number
 *
 * Return: the division of two numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function that get the reminder
 *
 * @a: The first number.
 * @b: The second number.
 *
 * Return: The reminder of the division of two numbers.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
